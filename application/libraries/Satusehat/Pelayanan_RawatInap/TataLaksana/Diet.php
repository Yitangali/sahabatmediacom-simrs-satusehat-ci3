    public function createNutritionOrderDiet(array $payload): array {
        $response = $this->client->request(
            'POST',
            'NutritionOrder',
            $payload
        );

        if (
            !isset($response['resourceType']) ||
            $response['resourceType'] !== 'NutritionOrder' ||
            empty($response['id'])
        ) {
            throw new Exception('Invalid NutritionOrder response (Diet)');
        }

        return [
            'id' => $response['id'],
        ];
    }

